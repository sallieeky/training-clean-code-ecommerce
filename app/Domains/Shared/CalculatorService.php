<?php

namespace App\Domains\Shared;

class CalculatorService
{
    public function add($a, $b)
    {
        return $a + $b;
    }

    public function subtract($a, $b)
    {
        return $a - $b;
    }

    public function multiply($a, $b)
    {
        return $a * $b;
    }

    public function divide($a, $b)
    {
        if ($b == 0) {
            return 'Unidentified';
        }

        return $a / $b;
    }
}
